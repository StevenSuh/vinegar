<script>
// Import the editor
import Icon from '@/components/Icon'
import { Editor, EditorContent, EditorMenuBar} from 'tiptap'
import {
  Blockquote,
  CodeBlock,
  HardBreak,
  Heading,
  OrderedList,
  BulletList,
  ListItem,
  TodoItem,
  TodoList,
  Bold,
  Code,
  Italic,
  Link,
  Strike,
  Underline,
  History,
} from 'tiptap-extensions'

export default {
    name: "editorTest",

    components: {
        EditorContent,
        EditorMenuBar,
        Icon
  
       
    },

    data() {
        return {
            editor: null,
            

        }
    },
    mounted() {
        this.editor = new Editor({
            content: '<p>This is just a boring paragraph</p>',
            autoFocus: true, 
            extensions: [
              new Blockquote(),
              new BulletList(),
              new CodeBlock(),
              new HardBreak(),
              new Heading({ levels: [1, 2, 3] }),
              new ListItem(),
              new OrderedList(),
              new TodoItem(),
              new TodoList(),
              new Bold(),
              new Code(),
              new Italic(),
              new Link(),
              new Strike(),
              new Underline(),
              new History(),
        ]

        })
    },
    beforeDestroy() {
        this.editor.destroy()
    },
}
</script>

